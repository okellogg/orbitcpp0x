void
IDLAny::skel_impl_ret_post (ostream          &ostr,
			    Indent           &indent,
			    const IDLTypedef *active_typedef) const
{
	ostr << indent << get_c_typename () << " c_ret = CORBA_any__alloc();" << endl;
	ostr << indent << "CORBA_any__copy (c_ret, _cpp_ret->_orbitcpp_cobj ());" << endl;
	ostr << indent << "delete _cpp_ret;" << endl;
	ostr << indent << "return c_ret;" << endl;
}


