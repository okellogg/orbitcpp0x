string
IDLAny::skel_decl_arg_get (const string     &c_id,
			   IDL_param_attr    direction,
			   const IDLTypedef *active_typedef) const
{
	//This should match the C skel generated by regular orbit-idl-2.
	
	std::string ret;

	switch (direction)
	{
	case IDL_PARAM_IN:
		ret = "const " + get_c_typename () + " " + c_id;
		break;
	case IDL_PARAM_INOUT:
		ret = get_c_typename () + " " + c_id;
		break;
	case IDL_PARAM_OUT:
		ret = get_c_typename () + "* " + c_id;
		break;
	}
	
	return ret;
}

void
IDLAny::skel_impl_arg_pre (ostream          &ostr,
			   Indent           &indent,
			   const string     &c_id,
			   IDL_param_attr    direction,
			   const IDLTypedef *active_typedef) const
{
	string cpp_id = "_cpp_" + c_id;
	string cpp_type = get_cpp_typename ();
	
	switch (direction)
	{
	case IDL_PARAM_IN:
	case IDL_PARAM_INOUT:   
		ostr << indent << cpp_type << " " << cpp_id << " = "
		     << "::CORBA::Any::_orbitcpp_wrap (" << c_id << ");"
		     << endl;
		break;
	case IDL_PARAM_OUT:
		ostr << indent << cpp_type << "_var " << cpp_id << ";" << endl;
		break;
	}    
}

